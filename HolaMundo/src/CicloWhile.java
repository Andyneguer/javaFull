public class CicloWhile {
    //Exercise #25---- Udemy Ciclo WHILE Y/O DO WHILE
    public static void main(String[] args) {
        //Ciclo While y do While
        var i=0;
        while (i<=3){
          System.out.println("i= " + i);
          i++;
          }

        do {
            System.out.println("i = " + i);
            i++;
        }while (i<=3);

    }
}
